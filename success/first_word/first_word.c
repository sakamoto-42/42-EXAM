#include <unistd.h>

int	main(int argc, char **argv)
{
	char	*str;

	if (argc - 1 == 1)
	{
		str = argv[1];
		while ((*str == ' ' || *str == '\t') && *str != '\0')
			str++;
		while (*str != ' ' && *str != '\t' && *str != '\0')
		{
			write(1, str, 1);
			str++;
		}
	}	
	write(1, "\n", 1);
}
