#include <unistd.h>

int	main(int argc, char **argv)
{
	char	*s;
	char	c;

	if (argc - 1 == 1)
	{
		s = argv[1];
		while (*s)
		{
			if (*s == '_')
			{
				if (*(s + 1))
				{
					c = *(s + 1) - 32;
					write(1, &c, 1);
					s++;
				}
			}
			else
				write(1, s, 1);
			s++;
		}
	}
	write(1, "\n", 1);
}
