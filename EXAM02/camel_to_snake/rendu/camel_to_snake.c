#include <unistd.h>

int	main(int argc, char **argv)
{
	char	c;

	if (argc - 1 == 1)
	{
		while (*argv[1])
		{
			c = *argv[1];
			if (c >= 'A' && c <= 'Z')
			{
				write(1, "_", 1);
				c += 32;
			}
			write(1, &c, 1);
			argv[1]++;
		}
		write(1, "\n", 1);
	}
}
