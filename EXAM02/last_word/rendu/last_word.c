#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	char	*s;

	if (argc - 1 == 1)
	{
		s = argv[1];
		i = 0;
		while (s[i])
			i++;
		i--;
		while (s[i] == ' ' || s[i] == '\t')
			i--;
		while (i >= 0 && !(s[i] == ' ' || s[i] == '\t'))
			i--;
		i++;
		while (s[i] && !(s[i] == ' ' || s[i] == '\t'))
		{
			write(1, &s[i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
}
