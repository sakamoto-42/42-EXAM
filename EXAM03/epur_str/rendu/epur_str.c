#include <unistd.h>

int	main(int argc, char **argv)
{
	char	*s;
	int		i;
	if (argc - 1 == 1)
	{
		s = argv[1];
		while (*s == ' ' || *s == '\t')
			s++;
		i = 0;
		while (s[i])
		{
			if (s[i] != ' ' && s[i] != '\t')
			{
				if (i != 0 && (s[i - 1] == ' ' || s[i - 1] == '\t'))
					write(1, " ", 1);
				write(1, &s[i], 1);
			}
			i++;
		}
	}
	write(1, "\n", 1);
}
